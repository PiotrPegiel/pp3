public class Books {
    private String name;
    private int cost;
    private String author;

    public void setName(String input){
        name = input;
    }
    public String getName(){
        return name;
    }

    public void setCost(int input){
        cost = input;
    }
    public int getCost(){
        return cost;
    }

    public void setAuthor(String input){
        author = input;
    }
    public String getAuthor(){
        return author;
    }
}
